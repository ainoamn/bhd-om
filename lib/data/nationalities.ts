/** قائمة مسطحة بجميع قيم الجنسيات (عربي وإنجليزي) للاستخدام في datalist */
export function getAllNationalityValues(locale?: string): string[] {
  const values = new Set<string>();
  NATIONALITIES.forEach((n) => {
    values.add(n.ar);
    values.add(n.en);
  });
  return Array.from(values).sort((a, b) => a.localeCompare(b, locale === 'ar' ? 'ar' : 'en'));
}
